package az.iktlab.lessons.lesson6;

public class MathFunctions {

    public static void main(String[] args) {

        int a = 5, b = 6;

        System.out.printf("Sqrt: %s\n", Math.sqrt(a));
        System.out.printf("Min: %s\n", Math.min(a, b));
        System.out.printf("Max: %s\n", Math.max(a, b));
        System.out.printf("Abs: %s\n", Math.abs(a));
        System.out.printf("Cos: %s\n", Math.acos(b));
        System.out.printf("Sin: %s\n", Math.sin(b));
    }
}
